import React, { useState, useEffect } from 'react';
import axios from 'axios';

const Payroll = () => {
  const [salarySlips, setSalarySlips] = useState([]);
  const [loading, setLoading] = useState(true);
  const [selectedSlip, setSelectedSlip] = useState(null);

  useEffect(() => {
    fetchSalarySlips();
  }, []);

  const fetchSalarySlips = async () => {
    try {
      setLoading(true);
      const token = localStorage.getItem('token');
      const response = await axios.get('http://localhost:5000/api/payroll/my-slips', {
        headers: { Authorization: `Bearer ${token}` }
      });

      console.log('Full Response:', response.data);
console.log('Success?', response.data.success);
console.log('Slips:', response.data.slips);

if (response.data.success) {
  setSalarySlips(response.data.slips);
}
    } catch (error) {
      console.error('Error fetching salary slips:', error);
      alert('Failed to load salary slips');
    } finally {
      setLoading(false);
    }
  };

  const formatMonth = (monthStr) => {
    const [year, month] = monthStr.split('-');
    const date = new Date(year, month - 1);
    return date.toLocaleDateString('en-US', { month: 'long', year: 'numeric' });
  };

  const formatCurrency = (amount) => {
    return 'â‚¹' + parseFloat(amount).toLocaleString('en-IN', { minimumFractionDigits: 2 });
  };

  const viewDetails = (slip) => {
    setSelectedSlip(slip);
  };

  const closeDetails = () => {
    setSelectedSlip(null);
  };

  const downloadPDF = (slip) => {
    // Generate simple text-based salary slip
    const content = `
=====================================
        SALARY SLIP
=====================================
Month: ${formatMonth(slip.month)}
Status: ${slip.status}
Generated: ${new Date(slip.generated_date).toLocaleDateString()}

-------------------------------------
EARNINGS
-------------------------------------
Basic Salary:        ${formatCurrency(slip.basic_salary)}
Allowances:          ${formatCurrency(slip.allowances)}
Overtime (${slip.overtime_hours}hrs): ${formatCurrency(slip.overtime_amount)}
                     ---------------
Gross Salary:        ${formatCurrency(slip.gross_salary)}

-------------------------------------
DEDUCTIONS
-------------------------------------
Absent Days: ${slip.absent_days}
Late Days: ${slip.late_days}
Half Days: ${slip.half_days}
Total Deductions:    ${formatCurrency(slip.deductions)}

-------------------------------------
NET SALARY:          ${formatCurrency(slip.net_salary)}
=====================================
    `;

    const blob = new Blob([content], { type: 'text/plain' });
    const url = window.URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `salary_slip_${slip.month}.txt`;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    window.URL.revokeObjectURL(url);
  };

  if (loading) {
    return (
      <div style={styles.container}>
        <div style={styles.loading}>Loading salary slips...</div>
      </div>
    );
  }

  return (
    <div style={styles.container}>
      <header style={styles.header}>
        <h1 style={styles.title}>ðŸ’° My Salary Slips</h1>
        <p style={styles.subtitle}>View and download your salary slips</p>
      </header>

      {salarySlips.length === 0 ? (
        <div style={styles.emptyState}>
          <div style={styles.emptyIcon}>ðŸ“„</div>
          <h3>No Salary Slips Yet</h3>
          <p>Your salary slips will appear here once generated by admin</p>
        </div>
      ) : (
        <div style={styles.slipGrid}>
          {salarySlips.map((slip) => (
            <div key={slip.payroll_id} style={styles.slipCard}>
              <div style={styles.slipHeader}>
                <h3 style={styles.monthTitle}>{formatMonth(slip.month)}</h3>
                <span style={{
                  ...styles.statusBadge,
                  backgroundColor: slip.status === 'Paid' ? '#10b981' : slip.status === 'Generated' ? '#3b82f6' : '#6b7280'
                }}>
                  {slip.status}
                </span>
              </div>

              <div style={styles.slipBody}>
                <div style={styles.salaryRow}>
                  <span style={styles.label}>Gross Salary:</span>
                  <span style={styles.value}>{formatCurrency(slip.gross_salary)}</span>
                </div>
                <div style={styles.salaryRow}>
                  <span style={styles.label}>Deductions:</span>
                  <span style={{...styles.value, color: '#ef4444'}}>- {formatCurrency(slip.deductions)}</span>
                </div>
                <div style={{...styles.salaryRow, ...styles.netSalary}}>
                  <span style={styles.label}>Net Salary:</span>
                  <span style={{...styles.value, fontSize: '20px', fontWeight: 'bold'}}>
                    {formatCurrency(slip.net_salary)}
                  </span>
                </div>

                <div style={styles.attendanceInfo}>
                  <div style={styles.attendanceItem}>
                    <span>Present: {slip.present_days}</span>
                  </div>
                  <div style={styles.attendanceItem}>
                    <span>Absent: {slip.absent_days}</span>
                  </div>
                  <div style={styles.attendanceItem}>
                    <span>Late: {slip.late_days}</span>
                  </div>
                  <div style={styles.attendanceItem}>
                    <span>Half: {slip.half_days}</span>
                  </div>
                </div>
              </div>

              <div style={styles.slipFooter}>
                <button 
                  onClick={() => viewDetails(slip)}
                  style={styles.viewButton}
                >
                  View Details
                </button>
                <button 
                  onClick={() => downloadPDF(slip)}
                  style={styles.downloadButton}
                >
                  ðŸ“¥ Download
                </button>
              </div>
            </div>
          ))}
        </div>
      )}

      {/* Details Modal */}
      {selectedSlip && (
        <div style={styles.modalOverlay} onClick={closeDetails}>
          <div style={styles.modal} onClick={(e) => e.stopPropagation()}>
            <div style={styles.modalHeader}>
              <h2>Salary Slip Details</h2>
              <button onClick={closeDetails} style={styles.closeButton}>âœ•</button>
            </div>
            
            <div style={styles.modalBody}>
              <div style={styles.detailSection}>
                <h3 style={styles.sectionTitle}>ðŸ“… Period</h3>
                <p>{formatMonth(selectedSlip.month)}</p>
              </div>

              <div style={styles.detailSection}>
                <h3 style={styles.sectionTitle}>ðŸ’µ Earnings</h3>
                <div style={styles.detailRow}>
                  <span>Basic Salary:</span>
                  <span>{formatCurrency(selectedSlip.basic_salary)}</span>
                </div>
                <div style={styles.detailRow}>
                  <span>Allowances:</span>
                  <span>{formatCurrency(selectedSlip.allowances)}</span>
                </div>
                <div style={styles.detailRow}>
                  <span>Overtime ({selectedSlip.overtime_hours} hrs):</span>
                  <span>{formatCurrency(selectedSlip.overtime_amount)}</span>
                </div>
                <div style={{...styles.detailRow, fontWeight: 'bold', borderTop: '2px solid #e5e7eb', paddingTop: '8px'}}>
                  <span>Gross Salary:</span>
                  <span>{formatCurrency(selectedSlip.gross_salary)}</span>
                </div>
              </div>

              <div style={styles.detailSection}>
                <h3 style={styles.sectionTitle}>ðŸ“‰ Deductions</h3>
                <div style={styles.detailRow}>
                  <span>Absent Days ({selectedSlip.absent_days}):</span>
                  <span style={{color: '#ef4444'}}>Deducted</span>
                </div>
                <div style={styles.detailRow}>
                  <span>Late Days ({selectedSlip.late_days}):</span>
                  <span style={{color: '#ef4444'}}>Penalty Applied</span>
                </div>
                <div style={styles.detailRow}>
                  <span>Half Days ({selectedSlip.half_days}):</span>
                  <span style={{color: '#ef4444'}}>Deducted</span>
                </div>
                <div style={{...styles.detailRow, fontWeight: 'bold', borderTop: '2px solid #e5e7eb', paddingTop: '8px'}}>
                  <span>Total Deductions:</span>
                  <span style={{color: '#ef4444'}}>{formatCurrency(selectedSlip.deductions)}</span>
                </div>
              </div>

              <div style={styles.netSalaryBox}>
                <h3>Net Salary</h3>
                <div style={styles.finalAmount}>{formatCurrency(selectedSlip.net_salary)}</div>
              </div>

              {selectedSlip.paid_date && (
                <div style={styles.paidInfo}>
                  âœ… Paid on {new Date(selectedSlip.paid_date).toLocaleDateString()}
                </div>
              )}
            </div>

            <div style={styles.modalFooter}>
              <button onClick={() => downloadPDF(selectedSlip)} style={styles.downloadButtonLarge}>
                ðŸ“¥ Download Slip
              </button>
            </div>
          </div>
        </div>
      )}
    </div>
  );
};

